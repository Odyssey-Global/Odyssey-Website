        <h5> Write a Strong and Compelling Summary </h5>
        <p>

          Your resume must include a succinct, strong executive summary right at the top. Keep it clear, concise and
          persuasive using no more
          than 30-40 words. It should clearly convey your USP and suitability to the recruiter. Think of the
          professional summary as a
          branding statement rather than an objective statement. </p>

      </div>
      <!-- End Col -->
    </div>
    </div>
