        <div class="col-10 col-md-8 col-lg-6 offset-1 offset-md-2 offset-lg-3">
          <h5> Keep Your Resume Short and Direct </h5>
          <p>The most important rule to writing an effective resume is keeping it short, to the point and direct. The
            general rule is to keep it no longer than 2 pages, unless you have good reason to make it any longer. Even
            if you are a highly experienced professional with a long career, it is best to highlight your work
            experience for the previous 10-15 years.
          </p>
        </div>
        <!-- End Col -->
      </div>
      <div class="row mb-3">
        <div class="col-10 col-md-8 col-lg-6 offset-1 offset-md-2 offset-lg-3">
          <h5> Tell Your Story Concisely </h5>
          <p>You must coherently narrate your story within the 2 pages of the resume. The storytelling in the resume
            must be precise, compelling and interesting. Text-heavy, wordy resumes will make the recruiter lose interest
            in getting to its end. So, it is best to write your resume in a way that it answers every question that a
            recruiter may have about your professional journey, story and skills.
          </p>
        </div>
        <!-- End Col -->
      </div>
      <div class="row mb-3">
        <div class="col-10 col-md-8 col-lg-6 offset-1 offset-md-2 offset-lg-3">
          <h5> Showcase Your Experience and Skills </h5>
          <p>You should show rather than tell the recruiter about your skills, accomplishments, etc. For instance, you
            could quantify your experience, skills, and accomplishments, wherever possible, using quantifiable metrics.
          </p>
          <p>You should use action verbs such as completed, managed, led, developed, created, sold, designed, achieved,
            etc. along with superlatives such as highest, fastest and best, among others. It is important to be
            assertive but not pompous.
          </p>
          <p>You can use infographics or other pictorial representations to showcase your skillset.
          </p>
        </div>
        <!-- End Col -->
      </div>
    </div>
    <div class="row mb-3">
      <div class="col-10 col-md-8 col-lg-6 offset-1 offset-md-2 offset-lg-3">
        <h5> Customize Your Resume to Each Job </h5>
        <p>Your resume is your marketing document, and it works best when it is tailored to target the job that you are
          applying to. So, ensure that you take your time to research the company and role thoroughly before applying
          for the job. Using these insights, you should tailor your resume to highlight all relevant aspects of your
          experience, skillset, accomplishments, etc. </p>
        <p>You should tell the recruiter how your vision and values align with the company’s values and culture. Find
          10-15 keywords from the job description to use in the resume. You could embolden them to highlight it to the
          recruiter. </p>

      </div>
      <!-- End Col -->
    </div>
